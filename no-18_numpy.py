'''There are two ways to import numpy:
     1.import numpy --> This will import the entire numpy module
     Syntax:
     import numpy
     array_name = numpy.array([Elements])
     Ex:
     roll = numpy.array([1,2,3,4,5])
     2.from numpy import* -->This will import all class,object,variable etc from numpy package.Here * means All.
     from numpy import*
     array_name = array([Elements])
     roll = array([1,2,3,4,5])
'''

